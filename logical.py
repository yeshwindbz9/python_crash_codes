# comparision operators: <, >, ==, !=, <=, >=
print("1>2: ",1>2)
print("1<2: ", 1<2)
print("1==2: ", 1==2)
print("1==1: ", 1==1)
print("1!=2: ", 1!=2)
print("3>=2: ", 3>=2)
print("3<=2: ", 3<=2)
# logical operators: and, or, not
print("1==1 and 2<3: ", 1==1 and 2>3)
print("1==1 and 2<3: ", 1==1 or 2<3)
print("1 not in [1, 2, 3]: ", 1 not in [1, 2, 3])